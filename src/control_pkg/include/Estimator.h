    AvgCov *                      _uCheck;
    // 输出的测量数据
    Eigen::Matrix<double, 12, 1>  _feetPos2Body;    // 腿相对与机身的位置 - 在{s}
    Eigen::Matrix<double, 12, 1>  _feetVel2Body;    // 腿相对与机身的速度 - 在{s}
    Eigen::Matrix<double,  4, 1>  _feetH;           // 腿相对与机身的高度 - 在{s}
